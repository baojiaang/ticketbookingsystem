    public boolean checkSame(){
        String input = input_new.getText();
        String again = input_again.getText();
        if(input.equals(again)){
            return true;
        }
        return false;
    }
    public boolean checkEmpty(){
        String input = input_new.getText();
        String again = input_again.getText();
        if(input.equals("")||input==null){
            return false;
        }
        return true;
    }
