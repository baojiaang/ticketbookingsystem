    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String input=input_new.getText();
        if(!checkSame()){
             JOptionPane.showMessageDialog(null, "two input not same!", "INFORMATION_MESSAGE",JOptionPane.INFORMATION_MESSAGE);
             return;
        }
        if(!checkEmpty()){
       JOptionPane.showMessageDialog(null, "password can not be empty!", "INFORMATION_MESSAGE",JOptionPane.INFORMATION_MESSAGE);
       return;
        }
        if(admin==null){
            userInfo.setPassword(input);
            try {
             int result=   new UserInfoDao().changePassword(userInfo);
             if(result!=0){
                   JOptionPane.showMessageDialog(null, "changed it!", "INFORMATION_MESSAGE",JOptionPane.INFORMATION_MESSAGE);
                   this.dispose();
               }
            } catch (SQLException ex) {
                Logger.getLogger(ChangePassword.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            admin.setPassword(input);
            try {
               int result= new AdminDao().changePassword(admin);
               if(result!=0){
                   JOptionPane.showMessageDialog(null, "changed it!", "INFORMATION_MESSAGE",JOptionPane.INFORMATION_MESSAGE);
                   this.dispose();
               }
            } catch (SQLException ex) {
                Logger.getLogger(ChangePassword.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

